#INTEGER
age = 21

#FLOATING-POINT
salary=3000.05
print("salary",salary,", type",type(salary))

#STRING
name="sabari"
print("name",name,",type",type(name),"\nage",age," ,type",type(age))

#BOOLEAN
is_alive=True
is_real=False
print("Boolean",type(is_real))

#LIST
list=[10,20,30,40]
print("List",list,",type",type(list))

#TUPLE
tup=(30,40,50)
print("Tuple",tup,",type",type(tup))

#SET - UNIQUE VAL ONLY
set={20,20,50,70}
print("Set",set,",type",type(set))

#DICTIONARY LIKE MAP
dic={'name':'sabari',   #WE CAN USE BOTH SINGLE AND DOUBLE QUOTES TO REP. STRINGS
     "age":21,
     "designation":"SE"}
print(dic)
print("type",type(dic))
